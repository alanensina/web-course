// Operador de atribuição '='

const a = 7
let b = 3

b += a // Soma e atribui: b = b + a
console.log(b)

b -= 4 // Subtrai e atribui: b = b - 4
console.log(b)

b *= 2 // Multiplica e atribui: b = b * 2
console.log(b)

b /= 2 // Divide e atribui: b = b / 2
console.log(b)

b %= 2 // Módulo e atribui: b = b % 2
console.log(b)

// Obs: Deve-se colocar a operação antes da atribuição

