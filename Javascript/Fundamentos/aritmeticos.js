const [a, b, c, d] = [20, 10, 5, 2]
console.log("Elementos:", a, b, c, d)

const soma = a + b + c + d
console.log("Resultado da soma dos 4 elementos é", soma)

const subtracao = a - b
console.log("O resultado da subtração de", a, "por", b, "será:", subtracao)

const multiplicacao = a * c
console.log("O resultado da multiplicação de", a, "por", c, "será:", multiplicacao)

const divisao = a / d
console.log("O resultado da divisão de", a, "por", d, "será:", divisao)

const modulo = a % d
console.log("O resto da divisão de", a, "por", d, "será:", modulo)

console.log("Resultados das operações com os operadores aritiméticos:",soma,subtracao,multiplicacao, divisao, modulo)